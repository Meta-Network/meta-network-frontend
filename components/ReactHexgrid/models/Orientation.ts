class Orientation {
  f0: any
  f1: any
  f2: any
  f3: any
  b0: any
  b1: any
  b2: any
  b3: any
  startAngle: any
  constructor(f0: any, f1: any, f2: any, f3: any, b0: any, b1: any, b2: any, b3: any, startAngle: any) {
    this.f0 = f0
    this.f1 = f1
    this.f2 = f2
    this.f3 = f3
    this.b0 = b0
    this.b1 = b1
    this.b2 = b2
    this.b3 = b3
    this.startAngle = startAngle
  }
}

export default Orientation